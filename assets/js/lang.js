function changeLanguage(lang) {
  localStorage.setItem('preferredLang', lang);
  
  document.querySelectorAll('[data-lang]').forEach(element => {
    const key = element.getAttribute('data-lang');
    if (translations[lang][key]) {
      if(key === 'live_desc' || key === 'polls_text') {
         if(key === 'polls_text') {
            const link = element.querySelector('a');
            if(link) {
              element.firstChild.textContent = translations[lang][key] + " ";
            } else {
               element.innerHTML = translations[lang][key]; 
            }
         } else {
            element.innerHTML = translations[lang][key];
         }
      } else {
        if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
            element.placeholder = translations[lang][key];
        } else {
            element.textContent = translations[lang][key];
        }
      }
    }
  });

  const imgElement = document.getElementById('current-lang-flag');
  if(imgElement) {
    imgElement.src = `assets/images/flag-${lang}.png`;
    imgElement.alt = lang === 'ca' ? 'Català' : 'Castellano';
  }
}

document.addEventListener('DOMContentLoaded', () => {
  // 1. INJECCIÓ DEL BÀNER DE COOKIES (Així no ho has de posar a l'HTML)
  if (!localStorage.getItem('cookiesAccepted')) {
    const banner = document.createElement('div');
    banner.id = 'cookie-banner';
    banner.className = 'fixed bottom-0 left-0 w-full bg-gray-900 text-white p-4 flex flex-col sm:flex-row justify-between items-center z-[9999] shadow-2xl';
    banner.innerHTML = `
      <div class="text-sm text-center sm:text-left mb-3 sm:mb-0 max-w-3xl font-poppins" data-lang="cookie_text"></div>
      <div class="flex items-center gap-4">
        <a href="privacitat.html" class="text-sm underline text-gray-300 hover:text-white transition" data-lang="cookie_info"></a>
        <button id="accept-cookies" class="bg-primary hover:bg-[#b13e1e] text-white font-bold py-2 px-6 rounded-full text-sm transition" data-lang="cookie_accept"></button>
      </div>
    `;
    document.body.appendChild(banner);
    
    document.getElementById('accept-cookies').addEventListener('click', () => {
      localStorage.setItem('cookiesAccepted', 'true');
      banner.remove();
    });
  }

  // 2. INICIALITZACIÓ DE L'IDIOMA
  const savedLang = localStorage.getItem('preferredLang') || 'ca';
  changeLanguage(savedLang);

  // 3. MENÚ DESPLEGABLE D'IDIOMES
  const langBtn = document.getElementById('lang-btn');
  const langDropdown = document.getElementById('lang-dropdown');
  if(langBtn) {
    langBtn.addEventListener('click', (e) => {
      e.stopPropagation();
      langDropdown.classList.toggle('show-lang');
    });
    window.addEventListener('click', () => {
      if (langDropdown.classList.contains('show-lang')) {
        langDropdown.classList.remove('show-lang');
      }
    });
  }
});
